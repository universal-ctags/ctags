INPUT=./parsers/typescript.c
OUTPUT=./parsers/d-typescript.h

exec > ${OUTPUT}
echo "/* Automatically generated by $0 */"
echo
echo '#ifndef CTAGS_PARSER_DEBUG_TYPESCRIPT_H'
echo '#define CTAGS_PARSER_DEBUG_TYPESCRIPT_H'
echo
echo '#ifdef DO_TRACING'
echo '#include "vstring.h"'
echo
${0%/*}/enumstr.sh ${INPUT} eTokenType decodeTokenType TOKEN_
${0%/*}/enumstr.sh ${INPUT} eKeywordId decodeKeywordId KEYWORD_
echo
echo 'static const char *decodeToken (const tokenInfo *t)'
echo '{'
echo '	static vString *buf;'
echo '	buf = vStringNewOrClearWithAutoRelease (buf);'
echo '	vStringCopyS (buf, "type: ");'
echo '	vStringCatS (buf, decodeTokenType (t->type));'
echo '	if (t->type == TOKEN_KEYWORD)'
echo '	{'
echo '		vStringCatS (buf, ", keyword: ");'
echo '		vStringCatS (buf, decodeKeywordId (t->keyword));'
echo '	}'
echo '	return vStringValue (buf);'
echo '}'
echo
echo '#endif /* DO_TRACING */'
echo '#endif /* CTAGS_PARSER_DEBUG_TYPESCRIPT_H */'
