error()
{
	echo "$@" 1>&2
	exit 1
}

verify_ctags()
{
	if ! [ -x ./ctags ]; then
		error "call after building ctags"
	fi
}

verify_topdir()
{
	case $(dirname "$1") in
		misc/debuggen|./misc/debuggen)
			:
			;;
		*)
			error "run $1 from the top of the ctags source tree"
			;;
	esac
}

gen_header()
{
	local script=$1
	local language=$(echo $2 | tr a-z A-Z)

	echo "/* Automatically generated by $script */"
	echo
	echo "#ifndef CTAGS_PARSER_DEBUG_${language}_H"
	echo "#define CTAGS_PARSER_DEBUG_${language}_H"
	echo
	echo '#ifdef DO_TRACING'
	echo '#include "vstring.h"'
	echo
}

gen_enumtbl()
{
	misc/enumstr.sh "$@"
	echo
}

gen_decodeToken()
{
	local tokenType=$1
	local typeMember=$2
	local decodeTypeFn=$3
	local typeKeyword=$4
	local keywordMember=$5
	local decodeKeywordFn=$6

	echo "static const char *decodeToken (const ${tokenType} *t)"
	echo '{'
	echo '	static vString *buf;'
	echo '	buf = vStringNewOrClearWithAutoRelease (buf);'
	echo '	vStringCopyS (buf, "type: ");'
	echo "	vStringCatS (buf, ${decodeTypeFn} (t->${typeMember}));"
	if [ "${typeKeyword}" != "-" ]; then
		echo "	if (t->${typeMember} == ${typeKeyword})"
		echo '	{'
		echo '		vStringCatS (buf, ", keyword: ");'
		echo "		vStringCatS (buf, ${decodeKeywordFn} (t->${keywordMember}));"
		echo '	}'
	fi
	echo '	return vStringValue (buf);'
	echo '}'
	echo

}

gen_footer()
{
	local language=$(echo $1 | tr a-z A-Z)

	echo
	echo '#endif /* DO_TRACING */'
	echo "#endif /* CTAGS_PARSER_DEBUG_${language}_H */"
}
